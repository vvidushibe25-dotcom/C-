#include <stdio.h>

int main() {
    printf("Demonstrating escape sequences in C:\n\n");

    printf("1. Using \\n:\n");
    printf("Hello\nWorld\n");
    
    printf("\n2. Using \\t:\n");
    printf("Name\tAge\tCity\n");
    printf("Alice\t20\tPatiala\n");
    printf("Bob\t22\tLudhiana\n");
    
    printf("\n3. Using \\b:\n");
    printf("ABCDE\bF\n"); 

    printf("\nFormatted output complete!\n");

    return 0;
}

